CREATE TABLE PASSENGER (
	B_ID INT NOT NULL,
    F_NAME VARCHAR(20) NOT NULL,
    M_NAME VARCHAR(20),
    L_NAME VARCHAR(20) NOT NULL,
    BIRTH DATE NOT NULL,
    EMAIL VARCHAR(50) NOT NULL,
    ADDRESS VARCHAR(100) NOT NULL,
    FOREIGN KEY (B_ID) REFERENCES BOOKING (ID)
);