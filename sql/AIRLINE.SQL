CREATE TABLE BOOKING (
	ID INT PRIMARY KEY AUTO_INCREMENT,
    BOOKING_DATE_TIME DATETIME NOT NULL,
    LUGGAGE INT NOT NULL DEFAULT 7,
    PRICE BIGINT,
    STATE VARCHAR(30) DEFAULT 'NOT CHECK IN',
